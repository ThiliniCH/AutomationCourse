package Calculator;

public class CalculatorLogic {

    public static int sum(int a, int b){
        return a+b;
    }

    public static int sub(int a, int b){
        return a-b;
    }

    public static int mul(int a, int b){
        return a*b;
    }

    public static int dev(int a, int b){
        return a/b;
    }
}
